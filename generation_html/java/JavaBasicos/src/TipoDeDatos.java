
public class TipoDeDatos {
	public static void main(String[]args) {
	 
	 //8 tipos de datos primitivos
	 byte numeroMasPequenio=100;
	 System.out.println(numeroMasPequenio);
	 
	 short numeroPequenio=32500;
	 System.out.println(numeroPequenio); 
	 
	 int numero=54;
	 System.out.println(numero);
	 long numeroGrande=123456789;
	 System.out.println(numeroGrande);
	 
	 float numeroDecimal=12345.2f;
	 System.out.println(numeroDecimal);
	 
	 double numeroDecimalGrande=12345.12;
	 System.out.println(numeroDecimalGrande);
	 
	 boolean verdadero=true; boolean falso=false;
	 System.out.println(verdadero);
	 System.out.println(falso);
	 
	 char letra= 'M';
	 System.out.println(letra);
	 
	 //Dato estructurado
	 String nombre= "Mario";
	 System.out.println(nombre);
	 
	 
	 
	
	 
	 
	 
	
	 
	 
	 
   }

}
